n = input('digite qualuqer coisa: ')
print('o tipo é: ', type(n))
print('Ele pode ser considerado um número? ', n.isnumeric())
print('ele pode ser considerado uma letra? ',n.isalpha())
print('ele pode ser considerado alfanumerico? ', n.isalnum())
print('ele está em caracteres minúsculos?', n.islower())
print('e maiúsculos?', n.isupper())
print('é um número decimal?', n.isdecimal())
print('é somente espaço? ', n.isspace())
print('ele é capitalizado? ', n.istitle())

